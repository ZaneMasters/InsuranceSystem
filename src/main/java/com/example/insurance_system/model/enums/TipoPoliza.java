package com.example.insurance_system.model.enums;

/**
 * Enumeración que representa los tipos de póliza que ofrece la aseguradora.
 */
public enum TipoPoliza {
    /** Seguro de vida. */
    VIDA,

    /** Seguro para vehículos (autos, motos). */
    VEHICULO,

    /** Seguro médico o de salud. */
    SALUD
}
